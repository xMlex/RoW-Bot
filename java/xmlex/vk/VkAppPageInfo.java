package xmlex.vk;

public class VkAppPageInfo {
    public String access_token, sid, secret, api_url, auth_key, ad_info;
    public int api_id, api_settings, viewer_id, user_id;

    public String getAccess_token() {
        return access_token;
    }

    public String getSid() {
        return sid;
    }

    public String getSecret() {
        return secret;
    }

    public String getApi_url() {
        return api_url;
    }

    public String getAuth_key() {
        return auth_key;
    }

    public String getAd_info() {
        return ad_info;
    }

    public int getApi_id() {
        return api_id;
    }

    public int getApi_settings() {
        return api_settings;
    }

    public int getViewer_id() {
        return viewer_id;
    }

    public int getUser_id() {
        return user_id;
    }
}
